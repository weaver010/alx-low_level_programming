#include<stdio.h>
/**
 * op_add  - add two num
 * @a:the a
 * @b:the b
 * Return:sum
 */
int op_add(int a, int b)
{
return (a + b);
}
/**
 * op_sub  - sub two num
 * @a:the a
 * @b:the b
 * Return:sub
 */
int op_sub(int a, int b)
{
return (a - b);
}
/**
 * op_mul- mul two num
 * @a:the a
 * @b:the b
 * Return:mul
 */
int op_mul(int a, int b)
{
return (a * b);
}
/**
 * op_div - div two num
 * @a:the a
 * @b:the b
 * Return:div
 */
int op_div(int a, int b)
{
return (a / b);
}
/**
 * op_mod  - mod two num
 * @a:the a
 * @b:the b
 * Return:mod
 */
int op_mod(int a, int b)
{
return (a % b);
}
